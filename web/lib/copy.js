/** ดึงคำภาษาไทยของศัพท์ (fallback = คำเดิม/ที่ส่งมา) */
export function termTh(key, fallback) {
  return GLOSSARY[key]?.th ?? fallback ?? key
}

/** ดึงคำอธิบาย 1 บรรทัดสำหรับ InfoTooltip */
export function termHint(key, fallback) {
  return GLOSSARY[key]?.hint ?? fallback ?? ''
}

/** ดึงทั้ง object ของศัพท์ (en/th/hint) */
export function gloss(key) {
  return GLOSSARY[key] ?? null
}

